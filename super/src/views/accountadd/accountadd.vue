<template>
<div class="accoun-add">
  <el-card class="box-card">
     <div slot="header" class="clearfix">
        <span>系统信息</span>
       
    </div>
    <!--添加账号表单-->
      <div class="text item">
           <el-form :model="userForm" status-icon :rules="rules" ref="userForm" label-width="100px" class="demo-ruleForm">
                <!-- 账号 -->
                <el-form-item label="账号" prop="username">
                    <el-input type="text" v-model="userForm.username" autocomplete="off"></el-input>
                </el-form-item>
                <!-- 密码 -->
                <el-form-item label="密码" prop="password">
                    <el-input type="text" v-model="userForm.password" autocomplete="off"></el-input>
                </el-form-item>
                <!-- 确认密码 -->
                <el-form-item label="确认密码" prop="checkPwd">
                    <el-input type="text" v-model="userForm.checkPwd" autocomplete="off"></el-input>
                </el-form-item>
                <!-- 活动区域 -->
                <el-form-item label="选择用户组" prop="activeArea">
                    <el-select v-model="userForm.activeArea" placeholder="请选择用户组">
                    <el-option label="普通用户" value="普通用户"></el-option>
                    <el-option label="高级用户" value="高级用户"></el-option>
                    </el-select>
                </el-form-item>
                <!-- 登录按钮&重置按钮 -->
                <el-form-item>
                    <el-button type="primary" @click="submitForm('userForm')">添加</el-button>
                    <el-button @click="resetForm('userForm')">重置</el-button>
                </el-form-item>
            </el-form>
      </div>
     
  </el-card>
</div>
</template>
<script>
import qs from 'qs';

export default {
  data() {
    // 包含特殊字符的函数
    const checkSpecificKey = str => {
      var specialKey =
        "[`~!#$^&*()=|{}':;',\\[\\].<>/?~！#￥……&*（）——|{}【】‘；：”“'。，、？]‘'";
      for (var i = 0; i < str.length; i++) {
        if (specialKey.indexOf(str.substr(i, 1)) != -1) {
          return false;
        }
      }
      return true;
    };

    // 验证密码的函数
    const pass = (rule, value, callback) => {
      if (value === "") {
        callback(new Error("请输入密码"));
      } else if (value.length < 3 || value.length > 6) {
        callback(new Error("密码长度 3 - 6 位"));
      } else if (!checkSpecificKey(value)) {
        callback(new Error("密码不能包含特殊字符"));
      } else {
        if (this.userForm.checkPwd !== "") {
          // 如果确认密码不为空
          this.$refs.userForm.validateField("checkPwd"); // 调用确认密码的验证（一致性验证）
        }
        // 成功的回调
        callback();
      }
    };

    // 确认密码的验证函数
    const checkPass = (rule, value, callback) => {
      // rule是传入的验证规则  value是用户输入的值 callback是一个回调函数
      if (value === "") {
        // 如果等于空
        callback(new Error("请再次输入密码")); // 输出提示
      } else if (value !== this.userForm.password) {
        // 如果确认密码 和 密码不同
        callback(new Error("两次输入密码不一致"));
      }
      // 如果直接调用 不传入任何错误信息 就是成功 绿色的勾勾
      callback();
    };

    return {
      // 登录表单的数据
      userForm: {
        username: "",
        password: "",
        checkPwd: "",
        activeArea: ""
      },
      // 验证的规则（一份数据）
      rules: {
        // 验证用户名
        username: [
          // 非空验证
          { required: true, message: "请输入账号", trigger: "blur" },
          { min: 3, max: 5, message: "账号长度在 3 - 5 位", trigger: "blur" }
        ],
        // 验证密码
        password: [
          // 非空验证
          { required: true, validator: pass, trigger: "blur" }
        ],
        // 验证确认密码
        checkPwd: [
          // 自定义验证函数
          { required: true, validator: checkPass, trigger: "blur" }
        ],
        // 活动区域验证
        activeArea: [
          { required: true, message: "请选择活动区域", trigger: "change" }
        ]

        /* 
        验证规则字段说明：
            { required: true/false 必填,   message: "错误的提示信息", trigger: "触发验证的方式" }
            { min: 最小长度, max: 最大长度, message: "错误的提示信息", trigger: "触发验证的方式" }
            { validator： 自定义验证规则函数名，  trigger: 'blur'}
        */
      }
    };
  },
  methods: {
    // 点击登录按钮 触发这个函数
    submitForm(formName) {
      // 获取表单组件 调用验证方法
      this.$refs[formName].validate(valid => {
        // 如果所有验证通过 valid就是true
        if (valid) {
          // alert("前端验证通过 可以提交给后端！");
          // 后续就可以把收集的账号和密码 一起发送给后端 验证用户名和密码的正确性。
          // 收集账号和密码
          let params = {
            username: this.userForm.username,
            password: this.userForm.password,
            activearea: this.userForm.activeArea
          };

         

          this.axios.post('http://127.0.0.1:666/account/accountadd', qs.stringify(params))
            .then(response => {
              // console.log(response);
              
              this.$message({
                type:"success",
                message:response.data.msg
              })
            })
            .catch(err => {
              console.log(err)
            })


          

          //  console.log(params)
          this.$router.push('/accountmanage')
        } else {
          // 否则就是false
         this.$message({
                type:"error",
                message:response.data.msg
              })
          return false;
        }
      });
    },
    // 点击重置按钮 触发这个函数
    resetForm(formName) {
      // this.$refs.userForm.resetFields() 获取整个表单组件 调用重置方法
      this.$refs[formName].resetFields();
    }
  }
};
</script>
